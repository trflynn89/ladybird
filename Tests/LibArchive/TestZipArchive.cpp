static ErrorOr<ByteString> create_scratch_directory()
{
    auto path = LexicalPath::join(Core::StandardPaths::tempfile_directory(), ByteString::formatted("test-zip-archive-{}", Core::System::getpid())).string();
    if (FileSystem::exists(path))
        TRY(FileSystem::remove(path, FileSystem::RecursionMode::Allowed));
    TRY(Core::Directory::create(path, Core::Directory::CreateDirectories::Yes));
    return path;
}

TEST_CASE(install_patches_into_optimized_zip)
{
    auto scratch_directory = TRY_OR_FAIL(create_scratch_directory());
    ScopeGuard guard { [&]() { (void)FileSystem::remove(scratch_directory, FileSystem::RecursionMode::Allowed); } };

    auto archive_path = LexicalPath::join(scratch_directory, "ladybird.zip"sv).string();
    {
        auto archive = TRY_OR_FAIL(Core::File::open(archive_path, Core::File::OpenMode::Write | Core::File::OpenMode::MustBeNew));
        TRY_OR_FAIL(archive->write_until_depleted(OPTIMIZED_ZIP));
    }

    auto original = TRY_OR_FAIL(Archive::ZipArchive::read_member(archive_path, "ladybird/original.txt"sv));
    EXPECT_EQ(original->bytes(), "Original archive entry.\n"sv.bytes());

    static auto const patches = to_array<Archive::ZipArchive::Patch>({
        { "ladybird/file1.txt"sv, "file1\n"sv.bytes() },
        { "ladybird/file2.txt"sv, "file2\n"sv.bytes() },
    });
    TRY_OR_FAIL(Archive::ZipArchive::install_patches(archive_path, patches));

    for (auto const& patch : patches) {
        auto member = TRY_OR_FAIL(Archive::ZipArchive::read_member(archive_path, patch.archive_path));
        EXPECT_EQ(member->bytes(), patch.contents);
    }

    original = TRY_OR_FAIL(Archive::ZipArchive::read_member(archive_path, "ladybird/original.txt"sv));
    EXPECT_EQ(original->bytes(), "Original archive entry.\n"sv.bytes());
}
