static ErrorOr<ByteString> create_scratch_directory()
{
    auto path = LexicalPath::join(Core::StandardPaths::tempfile_directory(), ByteString::formatted("test-tar-extractor-{}", Core::System::getpid())).string();
    if (FileSystem::exists(path))
        TRY(FileSystem::remove(path, FileSystem::RecursionMode::Allowed));
    TRY(Core::Directory::create(path, Core::Directory::CreateDirectories::Yes));
    return path;
}

TEST_CASE(extract_xz_compressed_tar_archive)
{
    auto scratch_directory = TRY_OR_FAIL(create_scratch_directory());
    ScopeGuard guard { [&]() { (void)FileSystem::remove(scratch_directory, FileSystem::RecursionMode::Allowed); } };

    auto archive_path = LexicalPath::join(scratch_directory, "ladybird.tar.xz"sv).string();
    {
        auto archive = TRY_OR_FAIL(Core::File::open(archive_path, Core::File::OpenMode::Write | Core::File::OpenMode::MustBeNew));
        TRY_OR_FAIL(archive->write_until_depleted(VALID_TAR_XZ.span()));
    }

    auto destination = LexicalPath::join(scratch_directory, "extracted"sv);
    TRY_OR_FAIL(Archive::TarExtractor::extract(archive_path, destination.string()));

    auto readme_path = destination.append("ladybird/readme.txt"sv);
    auto readme_file = TRY_OR_FAIL(Core::File::open(readme_path.string(), Core::File::OpenMode::Read));
    auto readme = TRY_OR_FAIL(readme_file->read_until_eof());
    EXPECT_EQ(readme.bytes(), "Ladybird archive extraction test.\n"sv.bytes());

    auto executable_path = destination.append("ladybird/bin/tool"sv);
    auto executable_stat = TRY_OR_FAIL(Core::System::stat(executable_path.string()));
    EXPECT_EQ(executable_stat.st_mode & 0777u, 0755u);

    auto symlink_path = destination.append("ladybird/tool-link"sv);
    EXPECT(FileSystem::is_link(symlink_path.string()));
}
